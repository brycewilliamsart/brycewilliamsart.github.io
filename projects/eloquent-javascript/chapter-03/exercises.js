function min(x, y) {
//if x is smaller than y, return x, else retrun y
return x < y ? x : y;
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

//Must use a recursive call
//input: a whole number
//output: boolean
function isEven(num) {
  if (num === 0){
    return true;
  } else if (num === 1){
    return false;
  }
  else if (num < 0){
    return isEven(-num)
  }
  return isEven(num - 2);
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
//declare a variable count to return a number
let count = 0;
//loop the string
for (var i = 0; i < string.length; i++){
  //if the string[i] is === to <char> argument
  if (string[i] === char){
    //increase count
    count ++
  }
}
//return count
return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
let count = 0
for(var i = 0; i < string.length; i++){
  if (string[i] === 'B'){
    count ++
  }
} 
return count;
}

