// reverseArray ////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function reverseArray(array) {
let arr = [];
for (var i = 0; i < array.length; i++){
  arr.unshift(array[i])
}
return arr;
}

