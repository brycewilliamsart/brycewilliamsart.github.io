// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(start, end, step) {
//return an array with all numbers between start and end, inclusive of s and e
if (!step) step = 1;
let arr = []

if (start === end) {return arr}

else if (step > 0) {
    for (var i = start; i <= end; i += step)
      arr.push(i);
  } else {
    for (var i = start; i >= end; i += step)
      arr.push(i);
  }
  
  return arr;
}

